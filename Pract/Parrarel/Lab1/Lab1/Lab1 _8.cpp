#include <iostream>
#include <omp.h>

using namespace std;

int main() {
    setlocale(LC_ALL, "RUS");
  
    // ЗАДАНИЕ 8 
    cout << "Задание 8." << endl;
    // ШАГ 1: Разрешаем вложенные параллельные области УИИИИ
    omp_set_nested(1); // 1 = true, разрешаем вложенность
    cout << "Вложенные параллельные области РАЗРЕШЕНЫ" << endl;

    omp_set_num_threads(2); // Для внешней области 2 потока

    cout << "Внешняя параллельная область (2 потока)..." << endl;
#pragma omp parallel
    {
        int outer_id = omp_get_thread_num();

#pragma omp critical
        cout << "Внешний поток " << outer_id << " создает вложенную область" << endl;

        // Вложенная параллельная область
#pragma omp parallel
        {
            int inner_id = omp_get_thread_num();

#pragma omp critical
            cout << "    Внутренний поток " << inner_id
                << " порожден внешним потоком " << outer_id << endl;
        } // конец вложенной области

#pragma omp critical
        cout << "Внешний поток " << outer_id << " вернулся в последовательную секцию (после вложенной)" << endl;
    } // конец внешней области

    // ШАГ 2: Запрещаем вложенные параллельные области
    omp_set_nested(0); // 0 = false, запрещаем вложенность
    cout << endl << "Вложенные параллельные области ЗАПРЕЩЕНЫ" << endl;

    cout << "Внешняя параллельная область (2 потока)..." << endl;
#pragma omp parallel
    {
        int outer_id = omp_get_thread_num();

#pragma omp critical
        cout << "Внешний поток " << outer_id << " пытается создать вложенную область" << endl;

        // Попытка создать вложенную параллельную область (но она уже запрещена) пххпхпхпхпх
#pragma omp parallel
        {
            int inner_id = omp_get_thread_num();

#pragma omp critical
            cout << "    Внутренний поток " << inner_id
                << " (порожден внешним потоком " << outer_id << ")" << endl;
        } // конец "вложенной" области (на самом деле не вложенной) мдаээээ

#pragma omp critical
        cout << "Внешний поток " << outer_id << " завершил попытку" << endl;
    } // конец внешней области

    cout << "Программа завершена." << endl;
    return 0;
    //
}
