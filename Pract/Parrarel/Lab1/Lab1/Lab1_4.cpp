#include <iostream>
#include <omp.h>

using namespace std;

int main() {
    setlocale(LC_ALL, "RUS");
 
    // ЗДАНИЕ 4
    cout << "Задание 4." << endl;

    omp_set_num_threads(4); // Устанавливаем 4 потока

    cout << "Программа запущена. Количество доступных потоков: "
        << omp_get_max_threads() << endl;
    cout << "Начало параллельной области (4 потока)..." << endl;

#pragma omp parallel
    {
        int thread_id = omp_get_thread_num();
        int num_threads = omp_get_num_threads();

#pragma omp critical
        {
            cout << "Поток " << thread_id << " из " << num_threads
                << " вошел в параллельную область" << endl;
        }

        // Имитация работы с распределением итераций
#pragma omp for
        for (int i = 0; i < 8; i++) {
#pragma omp critical
            cout << "Поток " << thread_id << " выполняет итерацию " << i << endl;
        }
    }
    //
    return 0;
    //
}
